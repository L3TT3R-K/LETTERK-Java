    private void jBtnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnThemActionPerformed
        try {
        String ma = jTextMaSach.getText();
        String ten = jTextTenSach.getText();
        String tg = jTextTG.getText();
        String nxb = jTextNXB.getText();
        double gia = Double.parseDouble(jTextGia.getText());

        if (ma.isEmpty() || ten.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Thông tin không được để trống!");
            return;
        }

        Sach s = new Sach(ma, ten, tg, nxb, gia);
        if (danhSach.themSach(s)) {
            loadTable(); // Tự động làm mới bảng
            javax.swing.JOptionPane.showMessageDialog(this, "Thêm thành công!");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Mã sách bị trùng!");
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Giá phải là số!");
    }
    }//GEN-LAST:event_jBtnThemActionPerformed

