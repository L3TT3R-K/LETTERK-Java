    private void jTableKQMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableKQMouseClicked
        int row = jTableKQ.getSelectedRow();
        if (row >= 0) {
            jTextMaSach.setText(jTableKQ.getValueAt(row, 0).toString());
            jTextTenSach.setText(jTableKQ.getValueAt(row, 1).toString());
            jTextTG.setText(jTableKQ.getValueAt(row, 2).toString());
            jTextNXB.setText(jTableKQ.getValueAt(row, 3).toString());
            jTextGia.setText(jTableKQ.getValueAt(row, 4).toString());

            jTextMaSach.setEditable(false); // KHÔNG ĐƯỢC PHÉP sửa mã sách
        }
    }//GEN-LAST:event_jTableKQMouseClicked

    private void jBtnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnXoaActionPerformed
        String ma = jTextMaSach.getText();
    if (ma.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn sách cần xóa!");
        return;
    }
    
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa?", "Xác nhận", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        if (danhSach.xoaSach(ma)) { // Gọi hàm xóa từ lớp DSSach của bạn
            loadTable(); // Hàm cập nhật lại bảng đã viết ở câu trước
            clearFields(); // Hàm xóa rỗng các ô nhập
            javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
        }
    }
    }//GEN-LAST:event_jBtnXoaActionPerformed

    private void jBtnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSuaActionPerformed
       try {
        String ma = jTextMaSach.getText();
        double gia = Double.parseDouble(jTextGia.getText());
        Sach s = new Sach(ma, jTextTenSach.getText(), jTextTG.getText(), jTextNXB.getText(), gia);
        
        if (javax.swing.JOptionPane.showConfirmDialog(this, "Xác nhận sửa?") == 0) {
            if (danhSach.capNhat(ma, s)) { loadTable(); clearFields(); }
        }
      } catch (Exception e) { javax.swing.JOptionPane.showMessageDialog(this, "Lỗi dữ liệu!"); }
    }//GEN-LAST:event_jBtnSuaActionPerformed

    private void jBtnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnLuuActionPerformed
        try (java.io.ObjectOutputStream oos = new java.io.ObjectOutputStream(new java.io.FileOutputStream("sach.dat"))) {
        oos.writeObject(danhSach.getDs());
        javax.swing.JOptionPane.showMessageDialog(this, "Đã lưu vào file!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Lỗi khi lưu file: " + e.getMessage());
    }
    }//GEN-LAST:event_jBtnLuuActionPerformed

    private void jBtnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnTimActionPerformed
        String ma = jTextMaSach.getText();
    if (ma.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sách để tìm!");
        return;
    }
    Sach s = danhSach.timSach(ma);
    if (s != null) {
        jTextTenSach.setText(s.getTenSach());
        jTextTG.setText(s.getTacGia());
        jTextNXB.setText(s.getNhaXuatBan());
        jTextGia.setText(String.valueOf(s.getGia()));
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Không tìm thấy sách!");
    }
    }//GEN-LAST:event_jBtnTimActionPerformed

